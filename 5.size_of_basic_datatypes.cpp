#include <iostream>
using namespace std;

int main()
{
cout << "\nSize of fundamental data types :\n";
cout << "The size of char datatype is : " << sizeof(char) << " bytes \n" ;
cout << "The size of short datatype is : " << sizeof(short) << " bytes \n" ;
cout << "The size of int datatype is : " << sizeof(int) << " bytes \n" ;
cout << "The size of long datatype is: " << sizeof(long) << " bytes \n" ;
cout << "The size of long long datatype is :" << sizeof(long long) << " bytes \n";
cout << "The size of float datatype is :" << sizeof(float) << " bytes \n" ;
cout << "The size of double datatype is :" << sizeof(double) << " bytes \n";
cout << "The size of long double datatype is :" << sizeof(long double) << " bytes \n";
cout << "The size of bool datatype is : " << sizeof(bool) << " bytes \n\n";
return 0;
}